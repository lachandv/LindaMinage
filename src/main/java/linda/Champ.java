package linda;

public class Champ {
	private String valeur;
	private String type;
	
	
	public Champ(String valeur, String type) {
		super();
		this.valeur = valeur;
		this.type = type;
	}
	
	public String getValeur() {
		return valeur;
	}
	public String getType() {
		return type;
	}

}
